import UIKit

class Utility {
    
    // Screen width.
    public static var screenWidth: CGFloat {
        return UIScreen.main.bounds.width
    }

    // Screen height.
    public static var screenHeight: CGFloat {
        return UIScreen.main.bounds.height
    }
}
